
//Circuit Board Price
#include<iostream>
#include<cmath>

using namespace std;

int main()
{
  const float PROFIT_PERCENT = 0.35; // Constant for markup rate
  double circuitBoardPrice = 14.95, sellingPrice = 0.0;  //Variable declaration and initialization

  sellingPrice = circuitBoardPrice * PROFIT_PERCENT; /* Setting sellingPrice equal to the value of a mathematical expression */

  cout<<"With a total cost of $"<<circuitBoardPrice<<", this circuit board's selling price is $"<<sellingPrice<<endl;


  return 0;
}
